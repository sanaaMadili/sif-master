    @Test
    @Transactional
    void putExistingArticle() throws Exception {
        // Initialize the database
        articleRepository.saveAndFlush(article);

        int databaseSizeBeforeUpdate = articleRepository.findAll().size();

        // Update the article
        Article updatedArticle = articleRepository.findById(article.getId()).get();
        // Disconnect from session so that the updates on updatedArticle are not directly saved in db
        em.detach(updatedArticle);
        updatedArticle
            .cai(UPDATED_CAI)
            .refPneu(UPDATED_REF_PNEU)
            .typePneu(UPDATED_TYPE_PNEU)
            .valeur(UPDATED_VALEUR)
            .image(UPDATED_IMAGE)
            .imageContentType(UPDATED_IMAGE_CONTENT_TYPE)
            .devise(UPDATED_DEVISE);

        restArticleMockMvc
            .perform(
                put(ENTITY_API_URL_ID, updatedArticle.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(updatedArticle))
            )
            .andExpect(status().isOk());

        // Validate the Article in the database
        List<Article> articleList = articleRepository.findAll();
        assertThat(articleList).hasSize(databaseSizeBeforeUpdate);
        Article testArticle = articleList.get(articleList.size() - 1);
        assertThat(testArticle.getCai()).isEqualTo(UPDATED_CAI);
        assertThat(testArticle.getRefPneu()).isEqualTo(UPDATED_REF_PNEU);
        assertThat(testArticle.getTypePneu()).isEqualTo(UPDATED_TYPE_PNEU);
        assertThat(testArticle.getValeur()).isEqualTo(UPDATED_VALEUR);
        assertThat(testArticle.getImage()).isEqualTo(UPDATED_IMAGE);
        assertThat(testArticle.getImageContentType()).isEqualTo(UPDATED_IMAGE_CONTENT_TYPE);
        assertThat(testArticle.getDevise()).isEqualTo(UPDATED_DEVISE);
    }

