    private static final String DEFAULT_TYPE_PNEU = "AAAAAAAAAA";
    private static final String UPDATED_TYPE_PNEU = "BBBBBBBBBB";

    private static final Double DEFAULT_VALEUR = 1D;
    private static final Double UPDATED_VALEUR = 2D;

    private static final byte[] DEFAULT_IMAGE = TestUtil.createByteArray(1, "0");
    private static final byte[] UPDATED_IMAGE = TestUtil.createByteArray(1, "1");
